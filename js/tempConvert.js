
// equation F to C     T(°C) = (T(°F) - 32) / (9/5)



var convertFtoC = function (fahr) {

  var celsius = ((fahr - 32) / (9/5));
  console.log(fahr + " degrees Fahrenheit is " + celsius + " degrees Celsius.");
};
convertFtoC(32);

